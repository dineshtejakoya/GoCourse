package intermediate

import (
	"bufio"
	"fmt"
	"os"
)

// os and bufio packages provide functionality of reading file
func main() {
	file, err := os.Open("output.txt")
	if err != nil {
		fmt.Println("Error Opening file:", err)
		return
	}
	defer func() {
		fmt.Println("Closing open file")
		file.Close()
	}()
	fmt.Println("File Opened Successfully")

	// //Read the contents of the opened file
	// data := make([]byte, 1024) //Buffer to read data into
	// _, err = file.Read(data)
	// if err != nil {
	// 	fmt.Println("Error reading data from file:", err)
	// 	return
	// }

	// fmt.Println("File content:", string(data))

	//we are going to create a scanner to read the file line by line using bufio package
	scanner := bufio.NewScanner(file)

	//Read line by line
	for scanner.Scan() {
		//scanner.text() returns the most recent token  generated by the caller scanner.scan as a newly allocated string holding its bytes
		line := scanner.Text()
		fmt.Println("Line:", line)
	}

	err = scanner.Err()
	if err != nil {
		fmt.Println("Error Reading the file :", err)
		return
	}
}
